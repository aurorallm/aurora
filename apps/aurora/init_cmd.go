func runInitSubcommand(args []string, stdout, stderr io.Writer) (handled bool, exitCode int) {
	if len(args) < 1 || args[0] != "init" {
		return false, 0
	}

	fs := flag.NewFlagSet("init", flag.ContinueOnError)
	fs.SetOutput(stderr)
	dir := fs.String("dir", ".", "Target directory to initialize")
	force := fs.Bool("force", false, "Overwrite existing files")
	withModels := fs.Bool("with-models", false, "Also download model registry")
	if err := fs.Parse(args[1:]); err != nil {
		return true, 2
	}

	return true, initProject(*dir, *force, *withModels, stdout, stderr)
}

func initProject(dir string, force, withModels bool, stdout, stderr io.Writer) int {
	absDir, err := filepath.Abs(dir)
	if err != nil {
		fmt.Fprintf(stderr, "error: failed to resolve path: %v\n", err)
		return 1
	}

	if err := os.MkdirAll(dir, 0755); err != nil {
		fmt.Fprintf(stderr, "error: failed to create directory %s: %v\n", dir, err)
		return 1
	}

	origDir, _ := os.Getwd()
	if err := os.Chdir(dir); err != nil {
		fmt.Fprintf(stderr, "error: failed to enter directory %s: %v\n", dir, err)
		return 1
	}
	defer func() { _ = os.Chdir(origDir) }()

	type fileSpec struct {
		path    string
		content string
		label   string
	}

	files := []fileSpec{
		{"config.yaml", initConfigYAML, "config.yaml"},
		{".env", initEnvTemplate, ".env"},
	}
	stubs := []fileSpec{
		{"data/user_pricing.yaml", initPricingStub, "data/user_pricing.yaml"},
		{"configs/fallback.json", initFallbackStub, "configs/fallback.json"},
	}

	for _, f := range files {
		if _, err := os.Stat(f.path); err == nil && !force {
			fmt.Fprintf(stderr, "error: %s already exists (use -force to overwrite)\n", f.path)
			return 1
		}
		if err := os.WriteFile(f.path, []byte(f.content), 0644); err != nil {
			fmt.Fprintf(stderr, "error: failed to write %s: %v\n", f.path, err)
			return 1
		}
		fmt.Fprintf(stdout, "  created  %s\n", f.label)
	}

	for _, f := range stubs {
		if err := os.MkdirAll(filepath.Dir(f.path), 0755); err != nil {
			continue
		}
		if _, err := os.Stat(f.path); os.IsNotExist(err) {
			_ = os.WriteFile(f.path, []byte(f.content), 0644)
			fmt.Fprintf(stdout, "  created  %s\n", f.label)
		}
	}

	fmt.Fprintf(stdout, "\n  Aurora gateway initialized in %s\n", absDir)

	if withModels {
		fmt.Fprintf(stdout, "\n  Downloading model registry...\n")
		if code := modelsSyncCmd(nil, stdout, stderr); code != 0 {
			fmt.Fprintf(stdout, "  (skip models sync — run 'aurora models sync' later)\n")
		}
	}

	fmt.Fprintf(stdout, "\n  Next steps:\n")
	fmt.Fprintf(stdout, "    1. Edit .env with your API keys\n")
	fmt.Fprintf(stdout, "    2. (Optional) aurora models sync\n")
	fmt.Fprintf(stdout, "    3. Run: aurora\n")
	return 0
}

